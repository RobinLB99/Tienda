    private void cargarTabla(boolean filtrar, long id) {
        // Definir modelo de tabla
        DefaultTableModel tablaModel = new DefaultTableModel() {
            // Filas y columnas no editables.
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        // Establecer los nombres de las columnas
        String titulos[] = {"ID", "Nombre", "Precio", "Unidades disponibles"};
        tablaModel.setColumnIdentifiers(titulos);

        if (filtrar) {
            
            if (id == 0) {
                
                ventana.mensaje("Registro no encontrado", "warning", "El registro que usted esta buscando no se encontro\nVerifique que haya ingresado correctamente el dato.");
                txtFiltroNombre.grabFocus();
                
            } else {
                
                Producto prod = control.buscarProducto(id);
                Object[] objeto = {prod.getId(), prod.getNombre(), prod.getPrecio(), prod.getStock()};
                tablaModel.addRow(objeto);
                
            }
            
        } else {
            // Recorrer datos y mostrarlos
            if (productos != null) {
                for (Producto producto : productos) {

                    Object[] objeto = {producto.getId(), producto.getNombre(), producto.getPrecio(), producto.getStock()};
                    tablaModel.addRow(objeto);

                }
            }
        }

        dataTable.setModel(tablaModel);

    }
    
    
